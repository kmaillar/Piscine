int		ft_count_if(char **tab, int (*f)(char*))
{
	int		i;
	int		cmp;

	i = 0;
	cmp = 0;
	while (tab[i] != '\0')
	{
		if (f(tab[i]) == 1)
			cmp++;
		i++;
	}
	return (cmp);
}
